struct fastio
{
	static const int S=1e7;
	char rbuf[S+48],wbuf[S+48];int rpos,wpos,len;
	fastio() {rpos=len=wpos=0;}
	inline char Getchar()
	{
		if (rpos==len) rpos=0,len=fread(rbuf,1,S,stdin);
		if (!len) return EOF;
		return rbuf[rpos++];
	}
	template <class T> inline void Get(T &x)
	{
		char ch;bool f;T res;
		while (!isdigit(ch=Getchar()) && ch!='-') {}
		if (ch=='-') f=false,res=0; else f=true,res=ch-'0';
		while (isdigit(ch=Getchar())) res=res*10+ch-'0';
		x=(f?res:-res);
	}
	inline void getstring(char *s)
	{
		char ch;
		while ((ch=Getchar())<=32) {}
		for (;ch>32;ch=Getchar()) *s++=ch;
		*s='\0';
	}
	inline void flush() {fwrite(wbuf,1,wpos,stdout);fflush(stdout);wpos=0;}
	inline void Writechar(char ch)
	{
		if (wpos==S) flush();
		wbuf[wpos++]=ch;
	}
	template <class T> inline void Print(T x,char ch)
	{
		char s[20];int pt=0;
		if (x==0) s[++pt]='0';
		else
		{
			if (x<0) Writechar('-'),x=-x;
			while (x) s[++pt]='0'+x%10,x/=10;
		}
		while (pt) Writechar(s[pt--]);
		Writechar(ch);
	}
	inline void printstring(char *s)
	{
		int pt=1;
		while (s[pt]!='\0') Writechar(s[pt++]);
	}
}io;

template <class T> inline T myabs(T x) {return x>=0?x:-x;}
inline LL gcd(LL x,LL y) {return y==0?x:gcd(y,x%y);}

int main ()
{
	LL a,b,c,x,y,z,ca;io.Get(ca);
	while (ca--)
	{
		io.Get(a);io.Get(b);io.Get(c);
		io.Get(x);io.Get(y);io.Get(z);
		LL ans=1ll*x*(c-b)+1ll*y*(a-c)+1ll*z*(b-a),fans=a+b+c;
		LL g=gcd(myabs(ans),fans);
		ans/=g;fans/=g;
		if (fans==1) io.Print(ans,'\n'); else io.Print(ans,'/'),io.Print(fans,'\n');
	}
	io.flush();return 0;
}
