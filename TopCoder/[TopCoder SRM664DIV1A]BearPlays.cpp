class BearPlays
{
	int n;
	inline int quick_pow(int x,int y)
	{
		int res=1;
		while (y)
		{
			if (y&1) res=(1ll*res*x)%n,y--;
			x=(1ll*x*x)%n;y>>=1;
		}
		return res;
	}
	public:
		inline int pileSize(int x,int y,int k)
		{
			n=x+y;
			x=(1ll*x*quick_pow(2,k))%n;
			return min(x,n-x);
		}
};

