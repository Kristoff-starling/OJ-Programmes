inline int getint()
{
    char ch;int res;bool f;
    while (!isdigit(ch=getchar()) && ch!='-') {}
    if (ch=='-') f=false,res=0; else f=true,res=ch-'0';
    while (isdigit(ch=getchar())) res=res*10+ch-'0';
    return f?res:-res;
}

int cur[300048];
int head[300048],to[300048],nxt[300048],tot;LL f[300048];
inline void addedge(int s,int t,LL cap)
{
	to[++tot]=t;nxt[tot]=head[s];head[s]=tot;f[tot]=cap;
	to[++tot]=s;nxt[tot]=head[t];head[t]=tot;f[tot]=0;
}

int n,m,t;
int a[148][148];
int dx[]={-1,0,1,0},dy[]={0,1,0,-1};

inline int getind(int x,int y) {return (x-1)*m+y;}

queue<int> q;int depth[300048];
inline bool bfs()
{
	int i,x,y;
	for (i=0;i<=t;i++) depth[i]=-1;
	depth[0]=0;q.push(0);
	while (!q.empty())
	{
		x=q.front();q.pop();
		for (i=head[x];i;i=nxt[i])
		{
			y=to[i];
			if (depth[y]==-1 && f[i])
			{
				depth[y]=depth[x]+1;
				q.push(y);
			}
		}
	}
	if (depth[t]==-1) return false; else return true;
}

inline LL dfs(int x,LL maxf)
{
	if (x==t || !maxf) return maxf;
	int y;LL minf,now,ans=0;
	for (int &i=cur[x];i;i=nxt[i])
	{
		y=to[i];
		if (depth[y]==depth[x]+1 && f[i])
		{
			minf=min(maxf-ans,f[i]);
			now=dfs(y,minf);
			f[i]-=now;f[i^1]+=now;ans+=now;
		}
		if (ans>=maxf) return ans;
	}
	if (!ans) depth[x]=0;
	return ans;
}

inline bool check(LL curans)
{
	int i,j,dir,x,y;LL need=0;
	tot=1;for (i=0;i<=t;i++) head[i]=0;
	for (i=1;i<=n;i++)
		for (j=1;j<=m;j++)
			if ((i+j-1)&1) need+=curans-a[i][j],addedge(0,getind(i,j),curans-a[i][j]); else addedge(getind(i,j),t,curans-a[i][j]);
	for (i=1;i<=n;i++)
		for (j=1;j<=m;j++)
			if ((i+j-1)&1)
				for (dir=0;dir<=3;dir++)
				{
					x=i+dx[dir];y=j+dy[dir];
					if (1<=x && x<=n && 1<=y && y<=m) addedge(getind(i,j),getind(x,y),LINF);
				}
	LL ans=0;
	while (bfs())
	{
		for (i=0;i<=t;i++) cur[i]=head[i];
		ans+=dfs(0,1ll<<62);
	}
	if (ans<need) return false; else return true;
}

int main ()
{
	//freopen ("a.in","r",stdin);
	int ca;ca=getint();
	int i,j,maxn;LL sum1,sum2;
	while (ca--)
	{
		n=getint();m=getint();t=n*m+1;
		sum1=0;sum2=0;maxn=-1;
		for (i=1;i<=n;i++)
			for (j=1;j<=m;j++)
			{
				a[i][j]=getint();
				if ((i+j-1)&1) sum1+=a[i][j]; else sum2+=a[i][j];
				maxn=max(maxn,a[i][j]);
			}
		if ((n&1) && (m&1))
		{
			if (sum1-sum2<maxn) {printf("-1\n");continue;}
			if (check(sum1-sum2)) printf("%lld\n",(1ll*n*m*(sum1-sum2)-(sum1+sum2))/2); else printf("-1\n");
			continue;
		}
		else
		{
			if (sum1!=sum2) {printf("-1\n");continue;}
			LL L=maxn,R=LINF,mid,ans=-1;
			while (L<=R)
			{
				mid=(L+R)>>1;
				if (check(mid)) ans=mid,R=mid-1; else L=mid+1;
			}
			if (ans==-1)
				printf("-1\n");
			else
				printf("%lld\n",(1ll*n*m*ans-sum1-sum2)/2);
		}
	}
	return 0;
}
